package com.raindropcentral.rdq.model.quest;

/**
 * Enumeration representing the difficulty levels of quest tasks.
 *
 * @author RaindropCentral
 * @version 2.0.0
 */
public enum TaskDifficulty {
    /**
     * Trivial difficulty - extremely easy tasks.
     */
    TRIVIAL,

    /**
     * Easy difficulty - simple tasks.
     */
    EASY,

    /**
     * Medium difficulty - moderate tasks.
     */
    MEDIUM,

    /**
     * Hard difficulty - challenging tasks.
     */
    HARD,

    /**
     * Extreme difficulty - nearly impossible tasks.
     */
    EXTREME
}
