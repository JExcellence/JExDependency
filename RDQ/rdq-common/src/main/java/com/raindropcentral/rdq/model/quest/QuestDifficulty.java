package com.raindropcentral.rdq.model.quest;

/**
 * Enumeration representing the difficulty levels of quests.
 *
 * @author RaindropCentral
 * @version 2.0.0
 */
public enum QuestDifficulty {
    /**
     * Easy difficulty - suitable for beginners.
     */
    EASY,

    /**
     * Medium difficulty - requires some experience.
     */
    MEDIUM,

    /**
     * Hard difficulty - challenging for experienced players.
     */
    HARD,

    /**
     * Expert difficulty - extremely challenging.
     */
    EXPERT
}
